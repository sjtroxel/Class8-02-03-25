        // ** Simplified Syntax W/ Arrow Functions

// Regular Function:
const sum = function (a, b) {
        return a + b;
}

// Arrow Function:
const sumArrow = (a, b) => a + b;

console.log(sum (2.5, 3.5));
console.log(sumArrow(2.5, 3.5)); 


        // ** Arrow Functions W/ One Parameter

